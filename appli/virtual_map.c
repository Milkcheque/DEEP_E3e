/*
 * @brief 	Initialize the virtual map
 */
void initMap(void){
    settings.width = 600;
    settings.height = 400;
    for(uint16_t y=0; y<settings.height/10; y++){
        for(uint16_t x=0; x<settings.width/10; x++){
            if(map[y][x] == 1){
                createTile(x*10,y*10,10,10);
            }
            else if(map[y][x] == 5){
                setPosPlayer(x*10, y*10);
            }
        }
    }
}

/*
 * @brief 	Get the offset of the virtual map
 * @return 	offset: offset of the virtual map
 */
uint16_t * getOffsetDisplay(void){
    return &offsetDisplay;
}

/*
 * @brief 	Set the offset of the virtual map
 * @param 	x: x offset
 * @param 	y: y offset
 */
void setOffsetDisplay(uint16_t x, uint16_t y){
    offsetDisplay[0] = x;
    offsetDisplay[1] = y;
}

settings_t * getMapSettings(void){
    return &settings;
}
